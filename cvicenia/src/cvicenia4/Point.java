package cvicenia4;

public class Point {
	
	double xAxisValue;
	double yAxisValue;
	double zAxisValue;
	
	public Point() {
		this.xAxisValue = 0;
		this.yAxisValue = 0;
		this.zAxisValue = 0;
	}
	
	public Point(double xAxisValue, double yAxisValue, double zAxisValue) {
		this.xAxisValue = xAxisValue;
		this.yAxisValue = yAxisValue;
		this.zAxisValue = zAxisValue;		
	}
}
